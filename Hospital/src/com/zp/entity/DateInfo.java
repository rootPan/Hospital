package com.zp.entity;

public class DateInfo {
	private int month;
	private int day;
	private String week;
	private String time;
	private int sw;
	private int xw;
	private int ws;
	public int getSw() {
		return sw;
	}
	public void setSw(int sw) {
		this.sw = sw;
	}
	public int getXw() {
		return xw;
	}
	public void setXw(int xw) {
		this.xw = xw;
	}
	public int getWs() {
		return ws;
	}
	public void setWs(int ws) {
		this.ws = ws;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public String getWeek() {
		return week;
	}
	public void setWeek(String week) {
		this.week = week;
	}
	
}
